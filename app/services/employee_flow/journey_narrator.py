"""
journey_narrator.py
===================
Phase 11 — Final LLM pass: validates path coherence, generates human-readable
narrative for each of the 3 paths, and builds the visualization tree JSON that
the frontend renders as the branching tree (root → main branches → twigs).

Uses qwen/qwen3.5-122b-a10b (thinking ON) for the highest-quality final output.

Visualization semantics:
  - root node        : role goal (largest bubble, bold)
  - main branch node : critical/moderate gap skill (medium bubble, coloured border)
  - twig node        : dependency/prerequisite skill that feeds into a main branch
  - edge thickness   : proportional to gap severity (critical > moderate)

Emits Redis events: journey | start → journey | result
"""

